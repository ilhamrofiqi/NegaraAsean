package com.example.ilhamrofiqi.negaraasean;

public class Asean {
    private String name, remarks, photo, deskripsi, ibukota, bahasa, merdeka, matauang, luas;

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public String getRemarks(){
        return remarks;
    }

    public void setRemarks(String remarks){
        this.remarks = remarks;
    }

    public String getPhoto(){
        return photo;
    }

    public void setPhoto(String photo){
        this.photo = photo;
    }

    public String getDeskripsi(){
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi){
        this.deskripsi = deskripsi;
    }

    public String getIbukota(){ return ibukota; }

    public void setIbukota(String ibukota){
        this.ibukota = ibukota;
    }

    public String getBahasa(){
        return bahasa;
    }

    public void setBahasa(String bahasa){
        this.bahasa = bahasa;
    }

    public String getMerdeka(){
        return merdeka;
    }

    public void setMerdeka(String merdeka){
        this.merdeka = merdeka;
    }

    public String getMatauang(){
        return matauang;
    }

    public void setMatauang(String matauang){
        this.matauang = matauang;
    }

    public String getLuas(){
        return luas;
    }

    public void setLuas(String luas){
        this.luas = luas;
    }
}
